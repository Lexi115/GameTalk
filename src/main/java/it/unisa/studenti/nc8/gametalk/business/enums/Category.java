package it.unisa.studenti.nc8.gametalk.business.enums;

/**
 * Enum che rappresenta le categorie assegnabili a un thread.
 */
public enum Category {

    /** Categoria per i nuovi utenti dove presentarsi alla community. */
    Welcome,

    /** Categoria per discussioni generali. */
    General,

    /** Categoria per richieste di aiuto. */
    Help,

    /** Categoria per la segnalazione di bugs. */
    Bugs,

    /** Categoria per guide e tutorial. */
    Guides,

    /** Categoria dedicata ai memes e contenuti divertenti. */
    Memes,

    /** Categoria per annunci ufficiali da parte dello staff. */
    Announcements
}
